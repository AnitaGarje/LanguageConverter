    void fillRules(){
        DefaultListModel lm = new DefaultListModel();
        DefaultListModel lm2 = new DefaultListModel();
        for(SingleToken s : currRule.inputTokens){
            lm.addElement(s.index + "  " + s.token);
        }
        for(SingleToken s : currRule.outputTokens){
            lm2.addElement(s.index + "  " + s.token);
        }
        jList2.setModel(lm);
        jList4.setModel(lm2);
    }
    
    void fillList(){
        DefaultListModel lm = new DefaultListModel();
        DefaultListModel lm2 = new DefaultListModel();
        for(SingleTag s : parent.db.tagList){
            lm.addElement(s.tag);
            lm2.addElement(s.tag);
        }
        jList1.setModel(lm);
        jList3.setModel(lm2);
    }
    
    void addToken(SingleToken st, int dest){
        if(dest == 1){
            int cnt=0;
            for(SingleToken s : currRule.inputTokens){
                if(s.token.equals(st.token)) cnt++;
            }
            st.index = cnt;
            currRule.inputTokens.addElement(st);
        }else if(dest == 2){
            int cnt=0;
            for(SingleToken s : currRule.outputTokens){
                if(s.token.equals(st.token)) cnt++;
            }
            st.index = cnt;
            currRule.outputTokens.addElement(st);
        }
    }
    
    
