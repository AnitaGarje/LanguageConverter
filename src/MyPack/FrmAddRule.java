    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(currRule.inputTokens.isEmpty()){
            return;
        }
        if(currRule.outputTokens.isEmpty()){
            return;
        }
        
        parent.db.ruleList.addElement(currRule);
        parent.writeDatabase();
        parent.readDatabase();
        JOptionPane.showMessageDialog(this, "Rule Added Successfully!");
        currRule = new SingleRule();
        fillRules();
    }//GEN-LAST:event_jButton4ActionPerformed

